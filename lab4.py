products = {  # словник товарів з цінами
    "хліб": 25.50,
    "молоко": 38.00,
    "вино": 150.00,
    "сир": 50.00
}
def price_str(value):  # функція форматує число в рядок ціни
    return f"вартість: {value:.2f} грн"  # округлення до 2 знаків після крапки
def items_available(*names):  # функція перевіряє, чи є кожен товар у списку
    result = {}  # тут зберігатиметься інформація про доступність
    for name in names:  # перебираємо всі передані назви товарів
        result[name] = name in products  # True, якщо товар є в словнику
    return result  # повертаємо словник з результатами
def process_order():  # головна функція обробки замовлення
    user_input = input("Введіть перелік товарів через кому: ").strip().lower()  # отримуємо введення
    order = [p.strip() for p in user_input.split(",") if p.strip()]  # розділяємо на елементи і очищуємо пробіли
    availability = items_available(*order)  # перевіряємо, які товари існують у магазині
    if not all(availability.values()):  # якщо хоча б один товар недоступний
        print("Деяких товарів немає в каталозі:")
        for name, ok in availability.items():  # переглядаємо всі товари
            if not ok:  # якщо товар відсутній
                print(f" - {name} відсутній")  # повідомляємо про це
        return  # завершуємо функцію, бо замовлення неможливе
    total_sum = sum(products[item] for item in order)  # рахуємо загальну суму товарів у замовленні
    print(f"Усі товари доступні! Загальна сума: {price_str(total_sum)}")  # виводимо суму
    action = input("Оберіть дію ('купити' або 'ціна'): ").lower().strip()  # запит вибору дії
    if action == "купити":  # якщо користувач хоче купити
        print("Покупка успішна! Дякуємо")  # повідомлення про успішну покупку
    elif action == "ціна":  # якщо потрібно показати детальну ціну
        print("Ціни на вибрані товари:")
        for item in order:  # перебираємо кожен товар у замовленні
            print(f"{item}: {price_str(products[item])}")  # виводимо ціну окремо
    else:
        print("Команда не розпізнана.")  # якщо введено щось інше
def main():  # функція запуску основного процесу
    process_order()  # викликаємо головну логіку
if __name__ == '__main__':  # стандартна точка входу програми
    main()  # запуск програми
